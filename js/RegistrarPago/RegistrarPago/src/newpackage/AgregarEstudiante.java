    private void btnAgregarRegistroEstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarRegistroEstActionPerformed
        if (tbNombres1.getText().trim().isEmpty() || tbApellidos1.getText().trim().isEmpty() || tbDni1.getText().trim().isEmpty() || cbxSexo1.getSelectedItem() == null || tbDomicilio1.getText().trim().isEmpty() || tbProcedencia1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completar todos los campos requeridos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String nombres = tbNombres1.getText().trim();
        String apellidos = tbApellidos1.getText().trim();
        String dni = tbDni1.getText().trim();
        String sexo = cbxSexo1.getSelectedItem().toString();
        String domicilio = tbDomicilio1.getText().trim();
        String procedencia = tbProcedencia1.getText().trim();

        Connection conn = null;
        PreparedStatement pst = null;
        RegistrarEstudiante rp = new RegistrarEstudiante();
        
        try{
            conn = ConexionSQL.getConexion();
            
            if(modoEdicion) {
                String sql = "UPDATE tb_estudiante SET nombres = ?, apellidos = ?, dni = ?, sexo = ?, domicilio = ?, IE_procedencia = ? WHERE id = ?";
                pst = conn.prepareStatement(sql);
                pst.setString(1, nombres);
                pst.setString(2, apellidos);
                pst.setString(3, dni);
                pst.setString(4, sexo);
                pst.setString(5, domicilio);
                pst.setString(6, procedencia);
                pst.setInt(7, idEstudianteEditar);
                
                int filasActualizadas = pst.executeUpdate();
                if (filasActualizadas > 0) {
                    JOptionPane.showMessageDialog(this, "Estudiante actualizado correctamente.", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
                    rp.cargarEstudiantesBD();
                    reiniciarFormulario();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo actualizar el estudiante.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                String sql = "INSERT INTO tb_estudiante (nombres, apellidos, dni, sexo, domicilio, IE_procedencia) VALUES (?, ?, ?, ?, ?, ?)";
                
                pst = conn.prepareStatement(sql);
                
                pst.setString(1, nombres);
                pst.setString(2, apellidos);
                pst.setString(3, dni);
                pst.setString(4, sexo);
                pst.setString(5, domicilio);
                pst.setString(6, procedencia);

                int filasInsertadas = pst.executeUpdate();
                if (filasInsertadas > 0) {
                    JOptionPane.showMessageDialog(null, "Registro agregado correctamente", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
                    rp.cargarEstudiantesBD();
                    reiniciarFormulario();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo agregar el registro a la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al conectar o insertar en la base de datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (pst != null) pst.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
            
            }
        }
    }//GEN-LAST:event_btnAgregarRegistroEstActionPerformed

