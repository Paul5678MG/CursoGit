    public void cargarEstudiantesBD(){
        Connection conn = null;
        PreparedStatement pst = null;
        ResultSet rs = null;
        
        try {
            conn = ConexionSQL.getConexion();
            String sql = "SELECT * FROM tb_estudiante";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            TableEstudiantes.modeloEstudiantes.setRowCount(0);
            
            while (rs.next()) {
               TableEstudiantes.modeloEstudiantes.addRow(new Object[]{
                   rs.getString("id"),
                   rs.getString("nombres"),
                   rs.getString("apellidos"),
                   rs.getString("dni"),
                   rs.getString("sexo"),
                   rs.getString("domicilio"),
                   rs.getString("IE_procedencia")
               });
            }
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Error al cargar estudiantes: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally{
            try {
                if (rs != null) rs.close();
                if (pst != null) pst.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                
            }             
        }
    }
