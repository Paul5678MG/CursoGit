    private void btnEditar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditar2ActionPerformed
        int filaSeleccionada = tblEstudiantes.getSelectedRow();
        
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un estudiante a editar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirmacion = JOptionPane.showConfirmDialog(null, "¬øEst√°s seguro de que deseas editar a este estudiante?", "Confirmar acci√≥n", JOptionPane.YES_NO_OPTION);
        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }
        
        int idEstudiante = Integer.parseInt(tblEstudiantes.getValueAt(filaSeleccionada, 0).toString());
        String nombres = tblEstudiantes.getValueAt(filaSeleccionada, 1).toString();
        String apellidos = tblEstudiantes.getValueAt(filaSeleccionada, 2).toString();
        String dni = tblEstudiantes.getValueAt(filaSeleccionada, 3).toString();
        String sexo = tblEstudiantes.getValueAt(filaSeleccionada, 4).toString();
        String domicilio = tblEstudiantes.getValueAt(filaSeleccionada, 5).toString();
        String procedencia = tblEstudiantes.getValueAt(filaSeleccionada, 6).toString();
        
        AgregarEstudiante formEdicion = new AgregarEstudiante();
        formEdicion.setDatosParaEdicion(idEstudiante, nombres, apellidos, dni, sexo, domicilio, procedencia);
        formEdicion.setVisible(true);
    }//GEN-LAST:event_btnEditar2ActionPerformed

