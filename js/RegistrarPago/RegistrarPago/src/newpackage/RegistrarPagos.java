    private void btnBuscarPagosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPagosActionPerformed
        if (tbDniPagos.getText().trim().isEmpty() && cbxTipoPago.getSelectedItem() == null && dateFechaPagos.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese al menos un criterio de búsqueda (DNI, Tipo de pago o Fecha)", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Connection conn = null;
        PreparedStatement pst = null;
        ResultSet rs = null;
        
        try {
            conn = ConexionSQL.getConexion();
            String sql = "SELECT * FROM tb_pagos WHERE 1=1";

            if (!tbDniPagos.getText().trim().isEmpty()) {
                sql += " AND dni_estudiante LIKE ?";
            }
            if (cbxTipoPago.getSelectedItem() != null && !cbxTipoPago.getSelectedItem().toString().equalsIgnoreCase("Todos")) {
                sql += " AND tipo_pago LIKE ?";
            }
            if (!dateFechaPagos.getText().trim().isEmpty()) {
                sql += " AND fecha = ?";
            }

            pst = conn.prepareStatement(sql);
            
            int index = 1;
            
            if (!tbDniPagos.getText().trim().isEmpty()) {
                pst.setString(index++, "%" + tbDniPagos.getText().trim() + "%");
            }
            if (cbxTipoPago.getSelectedItem() != null && !cbxTipoPago.getSelectedItem().toString().equalsIgnoreCase("Todos")) {
                pst.setString(index++, cbxTipoPago.getSelectedItem().toString());
            }
            if (!dateFechaPagos.getText().trim().isEmpty()) {
                pst.setString(index++, dateFechaPagos.getText().trim());
            }
            rs = pst.executeQuery();
            TablePagos.modeloPagos.setRowCount(0);
            
            while (rs.next()) {
                TablePagos.modeloPagos.addRow(new Object[] {
                    rs.getString("id_pagos"),
                    rs.getString("dni_estudiante"),
                    rs.getString("mes"),
                    rs.getDouble("saldo"),
                    rs.getString("fecha"),
                    rs.getString("num_recibo"),
                    rs.getString("entidad_financiera"),
                    rs.getString("tipo_pago")
                });
            }         
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Error al buscar pagos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } 
        finally{
            try {
                if (rs != null) rs.close();
                if (pst != null) pst.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                    
            }           
        }
    }//GEN-LAST:event_btnBuscarPagosActionPerformed

