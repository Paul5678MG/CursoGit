    public void cargarPagosBD(){
        Connection conn = null;
        PreparedStatement pst = null;
        ResultSet rs = null;
        
        try {
            conn = ConexionSQL.getConexion();
            String sql = "SELECT * FROM tb_pagos";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            TablePagos.modeloPagos.setRowCount(0);
            
            while (rs.next()) {
               TablePagos.modeloPagos.addRow(new Object[]{
                    rs.getString("id_pagos"),
                    rs.getString("dni_estudiante"),
                    rs.getString("mes"),
                    rs.getString("saldo"),
                    rs.getString("fecha"),
                    rs.getString("num_recibo"),
                    rs.getString("entidad_financiera"),
                    rs.getString("tipo_pago")
                });
            }
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Error al cargar pagos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally{
            try {
                if (rs != null) rs.close();
                if (pst != null) pst.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                
            }             
        }
    }

