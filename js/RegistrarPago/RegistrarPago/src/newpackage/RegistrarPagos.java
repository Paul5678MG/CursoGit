    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tblPagos.getSelectedRow();
        
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un pago a eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que deseas eliminar este pago?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }
        
        int idPago = Integer.parseInt(tblPagos.getValueAt(filaSeleccionada, 0).toString());

        Connection conn = null;
        PreparedStatement pst = null;
        
        try{
            conn = ConexionSQL.getConexion();
            String sql = "DELETE FROM tb_pagos WHERE id_pagos = ?";
            
            pst = conn.prepareStatement(sql);
            pst.setInt(1, idPago);
            
            int filasAfectadas = pst.executeUpdate();
            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(null, "Pago eliminado correctamente.");
                TablePagos.modeloPagos.removeRow(filaSeleccionada);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el pago a eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Error al eliminar el pago: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        finally{
            try {
                if (pst != null) pst.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int filaSeleccionada = tblPagos.getSelectedRow();
        
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un pago a editar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que deseas editar este pago?", "Confirmar acción", JOptionPane.YES_NO_OPTION);
        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }
        
        int idPago = Integer.parseInt(tblPagos.getValueAt(filaSeleccionada, 0).toString());
        String dni = tblPagos.getValueAt(filaSeleccionada, 1).toString();
        String mes = tblPagos.getValueAt(filaSeleccionada, 2) != null ? tblPagos.getValueAt(filaSeleccionada, 2).toString() : "";
        double saldo = Double.parseDouble(tblPagos.getValueAt(filaSeleccionada, 3).toString());
        String fecha = tblPagos.getValueAt(filaSeleccionada, 4).toString();
        String numRecibo = tblPagos.getValueAt(filaSeleccionada, 5).toString();
        String entidadFinanciera = tblPagos.getValueAt(filaSeleccionada, 6).toString();
        String tipoPago = tblPagos.getValueAt(filaSeleccionada, 7).toString();
        
        if (tipoPago.equalsIgnoreCase("Impresos")) {
            TipoImpresos impFrame = new TipoImpresos();
            impFrame.setDatosParaEdicion(idPago, dni, saldo, fecha, numRecibo, entidadFinanciera);
            impFrame.setVisible(true);
        } 
        else if (tipoPago.equalsIgnoreCase("Matrícula")) {
            TipoMatricula matFrame = new TipoMatricula();
            matFrame.setDatosParaEdicion(idPago, dni, saldo, fecha, numRecibo, entidadFinanciera);
            matFrame.setVisible(true);
        } 
        else if (tipoPago.equalsIgnoreCase("Pensiones")) {
            TipoPensiones penFrame = new TipoPensiones();
            penFrame.setDatosParaEdicion(idPago, dni, mes, saldo, fecha, numRecibo, entidadFinanciera);
            penFrame.setVisible(true);
        } 
        else {
            JOptionPane.showMessageDialog(null, "Tipo de pago no reconocido: " + tipoPago);
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

